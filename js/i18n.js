  get(key){
    return (this.t[this.lang]&&this.t[this.lang][key])||(this.t.en[key])||key;
  },

  apply(){
    document.querySelectorAll("[data-i18n]").forEach(el=>{
      el.textContent=this.get(el.dataset.i18n);
    });
    const btn=document.getElementById("langToggleBtn");
    if(btn) btn.textContent=this.lang==="en"?"日本語":"English";
    document.documentElement.lang=this.lang==="en"?"en":"ja";
    window.AppData.TOOLTIP_TEXT={
      convUnit:this.get("tooltip.convUnit"),
      distance:this.get("tooltip.distance"),
      sr:this.get("tooltip.sr"),
      beamAngle:this.get("tooltip.beamAngle"),
      reflectance:this.get("tooltip.reflectance")
    };
  },

  toggle(){
    this.lang=this.lang==="en"?"ja":"en";
    this.apply();
    if(window.AppUI) window.AppUI.updateGlobalModeUI();
  },

  init(){
    this.apply();
    const btn=document.getElementById("langToggleBtn");
    if(btn) btn.addEventListener("click",()=>this.toggle());
  }
